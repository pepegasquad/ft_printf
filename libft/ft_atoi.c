#include "libft.h"

int		ft_atoi(char *s)
{
    long	r;
    short	sign;

    r = 0;
    sign = 1;
    if (*s == '-' || *s == '+')
        sign = 44 - *s++;
    while (*s >= '0' && *s <= '9')
        r = r * 10 + *s++ - '0';
    return (sign * (int)r);
}